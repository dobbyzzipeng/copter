//SPIx 读写一个字节
//TxData:要写入的字节
//返回值:读取到的字节
u8 SPI1_ReadWriteByte(u8 TxData)
{		
	u8 retry=0;				 	
	while (SPI_I2S_GetFlagStatus(SPI1, SPI_I2S_FLAG_TXE) == RESET) //检查指定的SPI标志位设置与否:发送缓存空标志位
	{
	  retry++;
	  if(retry>200)return 0;
	}			  
	SPI_I2S_SendData(SPI1, TxData); //通过外设SPIx发送一个数据
	retry=0;

	while (SPI_I2S_GetFlagStatus(SPI1, SPI_I2S_FLAG_RXNE) == RESET)//检查指定的SPI标志位设置与否:接受缓存非空标志位
	{
	  retry++;
	  if(retry>200)return 0;
	}	  						    
	return SPI_I2S_ReceiveData(SPI1); //返回通过SPIx最近接收的数据					    
}
/******************************************************/
void SPI1_WriteOneByte(uint8_t SlaveAddr, uint8_t WriteAddr,uint8_t DataToWrite)
{
	uint8_t status;
	SPI1_CS=0;
	status=SPI1_ReadWriteByte(WriteAddr); //发送写命令+寄存器号
	SPI1_ReadWriteByte(DataToWrite);//写入寄存器值
	SPI1_CS=1;
}
/*******************************************************/
uint8_t SPI1_ReadOneByte(u8 SlaveAddr ,u8 ReadAddr)
{
	uint8_t reg_val;
	SPI1_CS=0;
	SPI1_ReadWriteByte(ReadAddr|0x80); //发送读命令+寄存器号
	reg_val=SPI1_ReadWriteByte(0xff);//读取寄存器值
	SPI1_CS=1;
	return(reg_val);
}
/*******************************************************/
void SPI1_ReadmultiyBytes(u8 SlaveAddr,u8 RegAddr,u8 Len,u8 *Buf_Addr)
{
	uint8_t i=0;
	SPI1_CS=0;
	SPI1_ReadWriteByte(RegAddr|0x80); //发送读命令+寄存器号
	for(i=0;i<Len;i++)
	{
	  *(Buf_Addr+i)=SPI1_ReadWriteByte(0xff);//读取寄存器值
	}
	SPI1_CS=1;
}
